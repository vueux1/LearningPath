namespace LearningPath.LearningPath._6_Loop._2_while_loop;

public class whileEx3
{
    //0 ile 100 arasındaki tek sayıları toplayarak sonucu ekrana getiren program
    //a program that adds odd numbers between 0 and 100 and displays the result
    static void Main(string[] args)
    {
        int i = 0, sum = 0;
        while (i <= 0)
        {
            if (i % 2 == 1)
            {
                sum += i++;
            }
        }
    }
}