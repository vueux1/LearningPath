namespace LearningPath.LearningPath._6_Loop._1_for_loop;

public class ForExample2
{
    //1 den 10 a kadar olan sayıları alt alta yazdıran program
    // A program that writes numbers from 1 to 10 below each other
    public static void Main()
    {
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }
    }
}