     What is a Stack?
A Stack is a virtual data type that stores data using a Last In, First Out (LIFO) logic. Data is added to or removed from the stack using two commands: push (add) and pop (remove). The removal of the first piece of data added to the Stack necessitates the removal of all subsequent data.

In a Stack, garbage collection is performed automatically. There is no need for the programmer to take any action in this regard.

When functions are called in a program, the local variables within the function are stored on the stack and are destroyed after the function terminates.

To access the data at the bottom of the Stack, you must remove all the data above it. The push command allows you to add data, while the pop command allows you to remove data.
     
     
     Stack Nedir?
Stack (yığın), LIFO (son eklenen ilk çıkar) mantığında çalışarak veri depolayan sanal bir veri türüdür. push(ekle) ve pop(çıkar) şeklindeki iki komut ile yığına veriler eklenir veya çıkarılır. Stack'e eklenen ilk verinin silinmesi sonrasındaki tüm verilerin silinmesine bağlıdır.

Stack'de çöp toplama (garbage collector) işlemi otomatik olarak gerçekleştirilir. Programcının bu konuda herhangi bir adım atmasına gerek yoktur.

Programda fonksiyonlar çağrıldığında fonksiyon içindeki yerel değişkenler (local variables) yığında(stack) tutulur ve fonksiyonun sonlanmasının ardından yok edilir.

