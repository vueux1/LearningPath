    /*
     Camel Case
    İsimlendirme çeşitlerinden biri olan camelCase burada yazıldığı şekilde ilk harfin küçük başlayıp 
    kelime ayrımlarından hemen sonra boşluk bırakmadan ikinci kelimenin büyük yazılmasıdır. Örnek verilirse:
    
    One of the naming conventions, camelCase, is written such that the first letter starts in lowercase and immediately after 
    a word separation, the second word starts with an uppercase letter, without leaving a space. For example:
    
    processPriority
    wordLength
    ledCount
    
    Pascal Case
    PascalCase ise yazım şeklinde görüldüğü gibi camelCase'den farklı olarak ilk harfin büyükle başlamasıdır. Örnek olarak:
     
    PascalCase, as seen in its writing style, differs from camelCase in that it starts with an uppercase letter. For example:

    OrderList
    IsBoolean
    MaxCount
    
    Snake Case
    Snake Case, ifadelerin boşluksuz yazıldığı, boşlukların alt çizgilerle (_) değiştirildiği ve kelimelerin genellikle tamamen küçük harfle yazıldığı bir yazım şeklidir. 
    kuyucuya görünümünü hatırlatmak için genellikle "snake_case" olarak stilize edilir. 
    Yılan biçimi, sıklıkla bir değişken isimlendirme kuralı olarak kullanılır. 
    Aşağıdaki isimler yılan biçimindedir:

    Snake case is a way of writing phrases without spaces, where spaces are replaced with underscores _, and the words are typically all lower case.
    It's often stylized as "snake_case" to remind the reader of its appearance.
    Snake casing is often used as a variable naming convention. 
    The following names are in snake case: 
    
    left_shift
    bitwise_invert
    matrix_transpose
     */
}