        Implicit type conversions are conversions that the C# program automatically performs without the risk of data loss.
        There is no need for the programmer to use a special expression for such conversions.
        Implicit type conversion is the automatic type conversion that occurs as a result of assigning a data type to a data type with a larger scope.
        This conversion is a situation depending on the scope of data types.
        For example, if a float variable is to be assigned to an int variable later, since the scope of the float data type includes the range of the int data type,
        there is no need for the programmer to use any type conversion expression when making such an assignment, and this operation is done automatically.
